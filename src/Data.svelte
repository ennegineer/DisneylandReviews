    <h3>Data Processing</h3>
    <p>
        The Kaggle data has review dates limited to just month and year. Other Disneyland locations were dropped from the data, leaving us with only Disneyland California reviews. <br /><br />
        The data from touringplans.com was scraped using Python and the beautifulsoup library. (<a href="https://github.com/ennegineer/DisneyETL/blob/main/scrape.ipynb" target="_blank">See the webcraping script in the original project repo here</a>)
